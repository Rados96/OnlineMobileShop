    private void btnSelectProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelectProductActionPerformed
        Session.getInstance().getUseCaseParams().put("request manufacturer", "PanelSelectProduct");
        Session.getInstance().getUseCaseParams().put("request type", "PanelSelectProduct");
        Session.getInstance().getUseCaseParams().put("request product", "PanelSelectProduct");
        Session.getInstance().getUseCaseParams().put("panelNewOrderItem", this);

        Session.getInstance().setCurrentUseCase("Select product");
        txtQuantity.setText("");
        panelSearchProduct = new PanelSearchProduct(ancestor);
        panelSearchProduct.setBtn();
        JDialog dialog = new MyForm(panelSearchProduct, "Pretraga i izbor proizvoda", true);
        dialog.add(panelSearchProduct);

        Controler.getInstance().setCurrentForm((IMyForm) dialog);
        prepareDialog(dialog);
    }//GEN-LAST:event_btnSelectProductActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
            validation();
            int quantity = Integer.parseInt(txtQuantity.getText().trim());
            double priceProduct = Double.parseDouble(txtPriceProduct.getText());
            double price = priceProduct * quantity;
            Product product = (Product) Session.getInstance().getUseCaseParams().get("selectedProduct");
            OrderItem oi = new OrderItem(null, -1, product, quantity, price);

            IMyForm imf = Controler.getInstance().getCurrentForm();
            PanelNewOrder panel = (PanelNewOrder) Session.getInstance().getUseCaseParams().get("panelNewOrder");

            panel.addOrderItem(oi);

            ((JDialog)imf).dispose();
            //Controler.getInstance().setCurrentForm(null);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }

    }//GEN-LAST:event_btnAddActionPerformed

