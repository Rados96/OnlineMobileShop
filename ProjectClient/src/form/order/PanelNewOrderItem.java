    private void prepareForm() {
        txtProduct.setEditable(false);
        txtPriceProduct.setEditable(false);
    }

    private void validation() throws Exception {
        if (txtQuantity.getText().isEmpty() || txtQuantity.getText().equals("")) {
            throw new Exception("Nije uneta koliƒçina!");
        } else if (Integer.parseInt(txtQuantity.getText().trim()) <= 0) {
            throw new Exception("Koliƒçina mora biti veƒáa od 0!");
        }

        if (txtProduct.getText().isEmpty() || txtProduct.getText().equals("")) {
            throw new Exception("Niste uneli proizvod!");
        }

    }

    private void prepareDialog(JDialog dialog) {
        Controler.getInstance().setActiveWindow(dialog);

        dialog.pack();
        dialog.setLocationRelativeTo(null);
        dialog.setVisible(true);
    }

    public void fillProduct(Product product) {
        txtProduct.setText(product+"");
        txtPriceProduct.setText(product.getPrice() + "");
    }

